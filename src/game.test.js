test('signToColor test', () => {
    expect(signToColor(1)).toBe('black');
    expect(signToColor(-1)).toBe('white');
})

test('colorToSign test', () => {
    let sign = 1;
    expect(colorToSign(signToColor(sign))).toBe(sign);
    sign = -1;
    expect(colorToSign(signToColor(sign))).toBe(sign)
})